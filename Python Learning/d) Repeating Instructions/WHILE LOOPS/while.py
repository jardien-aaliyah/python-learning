# WHILE LOOPS
# one of two loops in python (other is for loops)
# executes a set of statements if a condition is true

# print age as only if num is less than 25

# declaring variable
age = 1 # will start running from 1

while age < 26:
    print(age)
    # age += 1 #...runs all numbers from 1 to 25

    # BREAK STATEMENT
    if age == 15:
        break
    age += 1

