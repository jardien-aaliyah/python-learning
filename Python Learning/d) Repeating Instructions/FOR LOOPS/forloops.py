# FOR LOOPS
#  iterates over items in a list/data structure (step through)
#  repeats code blocks

# iterates over a sequence (list, tuple, dictionary, set, or string)

# creating list

word_list = ["box", "fox", "socks", "rocks"] # this is an array # simple list of 4 words

# item takes on the value of each element in the list
for word in word_list: # goes through each item one at a time
    print(word) # prints everything in my list

# Executing all the instructions in the block of repeating code (i.e. the code inside the loop) once is an iteration

# BREAK (stops execution of a loop, continues with other instruction in the loop)

# CONTINUE (skips the rest of instructions and starts with next iteration(repeat) of instructions in the loop)